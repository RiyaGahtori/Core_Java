package J1_IntroductionToJava.CJ5_TokenInJava;

public class T5_P2_AssignmentOperator {
    public static void main(String[] args)
    {
        int a ;
        a = 10; //Simple
        System.out.println(a);

        a+=10; // compound (a=a+10)
        System.out.println(a);

        a-=10; //Compound (a=a-10)
        System.out.println(a);
    }
}
